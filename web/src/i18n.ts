export type Lang = 'en' | 'zh'
export type TranslateParams = Record<string, string | number | boolean | null | undefined>
export type TFunction = (key: string, params?: TranslateParams) => string

export const LANG_STORAGE_KEY = 'sideplane.lang'

export function loadStoredLang(): Lang {
  try {
    const stored = window.localStorage.getItem(LANG_STORAGE_KEY)
    if (stored === 'en' || stored === 'zh') return stored
    return navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en'
  } catch {
    return 'en'
  }
}

