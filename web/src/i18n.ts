interface LanguageContextValue {
  lang: Lang
  setLang: (lang: Lang) => void
  t: TFunction
}

const defaultLanguageContext: LanguageContextValue = {
  lang: 'en',
  setLang: () => undefined,
  t: (key, params) => translate('en', key, params),
}

export const LanguageContext = createContext<LanguageContextValue>(defaultLanguageContext)

export function translate(lang: Lang, key: string, params?: TranslateParams): string {
  const template = translations[lang][key] ?? translations.en[key] ?? key
  if (!params) return template
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name: string) => {
    const value = params[name]
    return value === null || value === undefined ? match : String(value)
  })
}

export function LanguageProvider({
  children,
  lang,
  setLang,
}: {
  children: ReactNode
  lang: Lang
  setLang: (lang: Lang) => void
}) {
  const value = useMemo<LanguageContextValue>(() => ({
    lang,
    setLang,
    t: (key, params) => translate(lang, key, params),
  }), [lang, setLang])

  return createElement(LanguageContext.Provider, { value }, children)
}

export function useT(): LanguageContextValue {
  return useContext(LanguageContext)
}

export function formatRelativeTimeLabel(iso: string | undefined, t: TFunction): string {
  if (!iso?.trim()) return '-'

  const date = new Date(iso)
  if (Number.isNaN(date.getTime()) || date.getUTCFullYear() <= 1) return '-'

  const seconds = Math.max(0, Math.floor((Date.now() - date.getTime()) / 1000))
  if (seconds < 60) return t('time.secondsAgo', { count: seconds })
  const minutes = Math.floor(seconds / 60)
  if (minutes < 60) return t('time.minutesAgo', { count: minutes })
  const hours = Math.floor(minutes / 60)
  if (hours < 24) return t('time.hoursAgo', { count: hours })
  return t('time.daysAgo', { count: Math.floor(hours / 24) })
}
